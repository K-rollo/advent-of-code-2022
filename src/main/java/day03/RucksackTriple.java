package day03;

public class RucksackTriple {
    String firstRucksack;
    String secondRucksack;
    String thirdRucksack;

    public String getFirstRucksack() {
        return firstRucksack;
    }

    public void setFirstRucksack(String firstRucksack) {
        this.firstRucksack = firstRucksack;
    }

    public String getSecondRucksack() {
        return secondRucksack;
    }

    public void setSecondRucksack(String secondRucksack) {
        this.secondRucksack = secondRucksack;
    }

    public String getThirdRucksack() {
        return thirdRucksack;
    }

    public void setThirdRucksack(String thirdRucksack) {
        this.thirdRucksack = thirdRucksack;
    }
}
